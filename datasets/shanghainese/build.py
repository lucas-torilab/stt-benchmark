#!/usr/bin/env python3
"""Regenerate the derived artifacts of the Shanghainese eval dataset.

Source of truth (edit these):
  metadata.jsonl   one rich record per clip (transcription, gloss, part, flags, ...)
  audio_m4a/       original recordings, one <sample_id>.m4a per clip

Regenerated by this script (do not hand-edit):
  audio_pcm/       16 kHz mono 16-bit little-endian PCM, one <sample_id>.pcm per clip
  ground_truth.jsonl   repo-native: header + one {type:"sample", ...} row per clip
  manifest.csv         human-readable mapping table

Then package into a HuggingFace dataset (the form the benchmark loads) with:
  uv run python scripts/create_hf_dataset.py \
      --ground-truth datasets/shanghainese/ground_truth.jsonl \
      --audio-dir   datasets/shanghainese/audio_pcm \
      --output      datasets/shanghainese/hf_dataset

Requires macOS `afconvert` (ships with the OS) for m4a -> PCM. On Linux, swap the
convert command for: ffmpeg -i in.m4a -ac 1 -ar 16000 -f s16le out.pcm
"""

import csv
import json
import subprocess
import wave
from pathlib import Path

HERE = Path(__file__).resolve().parent
M4A_DIR = HERE / "audio_m4a"
PCM_DIR = HERE / "audio_pcm"
PCM_DIR.mkdir(exist_ok=True)
TARGET_RATE = 16000

LANGUAGE = "Shanghainese (Wu Chinese)"
LANGUAGE_CODE = "wuu"  # ISO 639-3 for Wu Chinese

rows = [json.loads(line) for line in (HERE / "metadata.jsonl").read_text(encoding="utf-8").splitlines() if line.strip()]


def convert(sample_id: str) -> float:
    """m4a -> 16 kHz mono int16 PCM. Returns duration in seconds."""
    src = M4A_DIR / f"{sample_id}.m4a"
    wav_tmp = PCM_DIR / f"{sample_id}.wav"
    subprocess.run(
        ["afconvert", "-f", "WAVE", "-d", f"LEI16@{TARGET_RATE}", "-c", "1", str(src), str(wav_tmp)],
        check=True, capture_output=True,
    )
    with wave.open(str(wav_tmp), "rb") as w:
        assert (w.getframerate(), w.getnchannels(), w.getsampwidth()) == (TARGET_RATE, 1, 2)
        pcm = w.readframes(w.getnframes())
    (PCM_DIR / f"{sample_id}.pcm").write_bytes(pcm)
    wav_tmp.unlink()
    return len(pcm) / 2 / TARGET_RATE


# Convert audio + refresh duration_seconds from the actual PCM; enforce language code.
for r in rows:
    r["duration_seconds"] = round(convert(r["sample_id"]), 3)
    r["language"] = LANGUAGE_CODE

# ground_truth.jsonl
with (HERE / "ground_truth.jsonl").open("w", encoding="utf-8") as f:
    f.write(json.dumps({
        "type": "header", "model": "human-script", "run_id": "shanghainese-v1",
        "num_samples": len(rows), "language": LANGUAGE, "language_code": LANGUAGE_CODE,
        "source": "Shanghai Chinese recording set (script-derived references)",
    }, ensure_ascii=False) + "\n")
    for r in rows:
        f.write(json.dumps({
            "type": "sample", "sample_id": r["sample_id"],
            "duration_seconds": r["duration_seconds"], "transcription": r["transcription"],
            "generated_at": "2026-06-30T00:00:00Z",
            "verified_by": "human" if r.get("verified_by") else None,
        }, ensure_ascii=False) + "\n")

# metadata.jsonl (rewritten so refreshed durations persist)
with (HERE / "metadata.jsonl").open("w", encoding="utf-8") as f:
    for r in rows:
        f.write(json.dumps(r, ensure_ascii=False) + "\n")

# manifest.csv
with (HERE / "manifest.csv").open("w", encoding="utf-8", newline="") as f:
    w = csv.writer(f)
    w.writerow(["sample_id", "kind", "part", "part_name", "item_no", "duration_seconds",
                "needs_verification", "meaning_en", "transcription", "original_file", "note"])
    for r in rows:
        w.writerow([r["sample_id"], r["kind"], r["part"], r["part_name"], r["item_no"],
                    r["duration_seconds"], r["needs_verification"], r["meaning_en"],
                    r["transcription"], r["original_file"], r.get("note") or ""])

total = sum(r["duration_seconds"] for r in rows)
flagged = [r["sample_id"] for r in rows if r["needs_verification"]]
print(f"Regenerated {len(rows)} samples ({total/60:.1f} min audio).")
print(f"Flagged needs_verification ({len(flagged)}): {', '.join(flagged)}")
